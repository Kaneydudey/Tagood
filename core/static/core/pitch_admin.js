(function () {
  function buildEditor(moraInput, startInput, endInput) {
    const wrap = document.createElement("div");
    wrap.className = "pitch-mora-wrap";

    const controls = document.createElement("div");
    controls.className = "pitch-controls";

    const status = document.createElement("div");
    status.textContent = "Click HIGH start, then HIGH end (can be same mora).";

    const resetBtn = document.createElement("button");
    resetBtn.type = "button";
    resetBtn.textContent = "Reset";

    controls.appendChild(resetBtn);
    controls.appendChild(status);

    moraInput.insertAdjacentElement("afterend", controls);
    controls.insertAdjacentElement("afterend", wrap);

    function clearSelection() {
      startInput.value = "";
      endInput.value = "";
      render();
    }

    function getMora() {
      const text = (moraInput.value || "").trim();
      if (!text) return [];
      return text.split(/\s+/);
    }

    function getSpan() {
      const s = startInput.value === "" ? null : parseInt(startInput.value, 10);
      const e = endInput.value === "" ? null : parseInt(endInput.value, 10);
      return { s, e };
    }

    function setStart(i) {
      startInput.value = String(i);
    }
    function setEnd(i) {
      endInput.value = String(i);
    }

    function render() {
      const mora = getMora();
      const { s, e } = getSpan();

      wrap.innerHTML = "";

      mora.forEach((m, idx) => {
        const chip = document.createElement("button");
        chip.type = "button";
        chip.className = "pitch-mora-chip";
        chip.textContent = m;

        const inRange = s !== null && e !== null && idx >= Math.min(s, e) && idx <= Math.max(s, e);
        if (inRange) chip.classList.add("selected");

        chip.addEventListener("click", () => {
          const span = getSpan();
          if (span.s === null) {
            setStart(idx);
            status.textContent = "Now click HIGH end (can be same mora).";
            render();
            return;
          }
          if (span.e === null) {
            // Allow same-mora end selection (start == end)
            setEnd(idx);

            // Normalize so start <= end
            const ns = parseInt(startInput.value, 10);
            const ne = parseInt(endInput.value, 10);
            if (ne < ns) {
              startInput.value = String(ne);
              endInput.value = String(ns);
            }

            status.textContent = "Span set. Reset to change.";
            render();
            return;
          }

          // Third click: reset (or user can use Reset button)
          clearSelection();
          status.textContent = "Click HIGH start, then HIGH end (can be same mora).";
        });

        wrap.appendChild(chip);
      });
    }

    resetBtn.addEventListener("click", () => {
      clearSelection();
      status.textContent = "Click HIGH start, then HIGH end (can be same mora).";
    });

    moraInput.addEventListener("input", () => {
      // If mora list changes, selection should be cleared
      clearSelection();
      status.textContent = "Click HIGH start, then HIGH end (can be same mora).";
    });

    render();
  }

  document.addEventListener("DOMContentLoaded", () => {
    // These IDs are generated by Django admin for our custom form field + model fields
    const moraInput = document.getElementById("id_mora_text");
    const startInput = document.getElementById("id_pitch_start");
    const endInput = document.getElementById("id_pitch_end");

    if (moraInput && startInput && endInput) {
      buildEditor(moraInput, startInput, endInput);
    }
  });
})();